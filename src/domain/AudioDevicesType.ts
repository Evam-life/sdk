
/**
 * Enum for the different audio device types supported for phone calls. 
 */
enum AudioDevicesType {
    /**
     * The device speaker.
     */
    SPEAKER = "SPEAKER",
    /**
     * Headset/headphones connected using headphone jack or USB
     */
    WIRED = "WIRED",
    /**
     * Any bluetooth headset/headphones or speaker
     */
    BLUETOOTH = "BLUETOOTH"
}

export  {AudioDevicesType};